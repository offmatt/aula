#include <stdio.h>

int main() {
    int opA = 2, opB = 5;

    printf("%d\n", opA == opB);
    printf("%d\n", opA > opB);
    printf("%d\n", opA >= opB);
    printf("%d\n", opA < opB);
    printf("%d\n", opA <= opB);
    printf("%d\n", opA != opB);

    system("PAUSE");
    return 0;

}
